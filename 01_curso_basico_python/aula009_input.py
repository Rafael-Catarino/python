# Programa que solicita dois números ao usuário e exibe a soma deles

# Solicita o primeiro número (aceita inteiros ou decimais)
num1 = float(input('Informe o primeiro número..: '))

# Solicita o segundo número (aceita inteiros ou decimais)
num2 = float(input('Informe o segundo número...: '))

# Calcula a soma dos dois números
soma = num1 + num2

# Exibe o resultado da soma formatado
print(f'A soma entre os números {num1} e {num2} é igual a: {soma}')
